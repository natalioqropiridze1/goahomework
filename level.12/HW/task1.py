#  >
print(5 > 3)   # True
print(10 > 20) # False
print(-1 > -5) # True
print(0 > 0)   # False
print(100 > 50)# True

#  >= 
print(5 >= 3)   # True
print(10 >= 10) # True
print(-1 >= -1) # True
print(0 >= 1)   # False
print(100 >= 200) # False

#  <
print(5 < 3)   # False
print(10 < 20) # True
print(-1 < -5) # False
print(0 < 1)   # True
print(100 < 100)# False

#  <= 
print(5 <= 3)   # False
print(10 <= 10) # True
print(-1 <= -1) # True
print(0 <= 1)   # True
print(100 <= 200) # True

#  == 
print(5 == 3)   # False
print(10 == 10) # True
print(-1 == -1) # True
print(0 == 1)   # False
print(100 == 100) # True

#  != 
print(5 != 3)   # True
print(10 != 10) # False
print(-1 != -1) # False
print(0 != 1)   # True
print(100 != 50) # True
